package EX09_Switch;

public class Lab085_Interview {
    public static void main(String[] args) {
        char code ='c';
        switch (code){
            default:
                System.out.println("default");
                break;
            case 'c':
                System.out.println("c");
                break;
            case 'b':
                System.out.println("b");
                break;
        }
    }
}
