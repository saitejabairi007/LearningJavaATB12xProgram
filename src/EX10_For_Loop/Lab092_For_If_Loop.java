package EX10_For_Loop;

public class Lab092_For_If_Loop {
    public static void main(String[] args) {
        for(int sai=0; sai<=18;sai++){
            if(sai>15){
                System.out.println("gift from father");
            }
            else{
                System.out.println("No gift");
            }
        }
    }
}
