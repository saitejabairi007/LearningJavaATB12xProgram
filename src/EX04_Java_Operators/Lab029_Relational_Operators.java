package EX04_Java_Operators;

public class Lab029_Relational_Operators {
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to ( checking)
        // != -> Not equal

        // All of them will result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

       int sai_age=26;
       int nikki_age=29;
       boolean age = sai_age<nikki_age;
        System.out.println(sai_age>nikki_age);
        System.out.println(age);

    }
}
